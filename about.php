<p align="left"><font color="black">
The objective of the system is to automate all the activities of the canteen
right from purchases to delivery of food/beverage items. The system should
maintain a detailed account of all provisions bought and food served at the
canteen. In addition to this, it should also maintain the daily expenses incurred
by the staff. The system should provide an interface to payroll for deductions.
Several inquiry facilities should also be provided to view the expenses
incurred/ planned menus/cash payment etc. 
</p>
<p align="left"><font color="black">
Canteen and Cafeteria in an organization are not merely food outlets, they are anti-monotony mechanisms in the corporate world.
 Food, friends and fun can boost up team sprit and help in overcoming work related pressures. 

Making staff canteens efficient and employee friendly requires a lot of thought and planning. Other than hygienic and healthy
 food, it also requires systems that ensure quick delivery of food and reliable usage accounting. If not managed properly, 
 it can become a huge financial liability for organizations. 
